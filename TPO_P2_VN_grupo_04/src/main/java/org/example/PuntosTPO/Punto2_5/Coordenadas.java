package org.example.PuntosTPO.Punto2_5;

//TDA Coordenadas
public class Coordenadas {

    private double x;
    private double y;

    //Inicializa coordenadas en un plano
    public Coordenadas(double x, double y) {
        this.x = x;
        this.y = y;
    }

    //Obtiene coordenada Eje X
    public double getX() {
        return x;
    }

    //Obtiene coordenada Eje Y
    public double getY() {
        return y;
    }


}



